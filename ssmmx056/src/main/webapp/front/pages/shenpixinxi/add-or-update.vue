			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`shenpixinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='shenpibianhao'){
					this.ruleForm.shenpibianhao = obj[o];
					this.ro.shenpibianhao = true;
					continue;
					}
					if(o=='biaoti'){
					this.ruleForm.biaoti = obj[o];
					this.ro.biaoti = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='shenpizhuangtai'){
					this.ruleForm.shenpizhuangtai = obj[o];
					this.ro.shenpizhuangtai = true;
					continue;
					}
					if(o=='shenpishijian'){
					this.ruleForm.shenpishijian = obj[o];
					this.ro.shenpishijian = true;
					continue;
